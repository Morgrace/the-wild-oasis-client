// Country-specific patterns
const namePatterns = {
  "united states": /^\d{3}-\d{2}-\d{4}$/,
  nigeria: /^\d{11}$/,
  india: /^\d{12}$/,
  "united kingdom": /^[A-CEGHJ-PR-TW-Z]{2}\d{6}[A-D]$/,
  canada: /^\d{3}\s?\d{3}\s?\d{3}$/,
  germany: /^[A-Z0-9]{9}$/,
  brazil: /^[0-9]{2}\.[0-9]{3}\.[0-9]{3}-[0-9X]$/,
  france: /^[12]\d{2}(0[1-9]|1[0-2])\d{2}\d{3}\d{3}\d{2}$/,
  australia: /^\d{8,9}$/,
  mexico: /^[A-Z]{4}\d{6}[HM][A-Z]{5}[A-Z0-9]\d$/,
  // …add more country-specific entries…
};

// A generic fallback: alphanumeric, hyphens allowed, 5–20 chars
const genericPattern = /^[A-Za-z0-9\-]{5,20}$/;

/**
 * Validate a National ID by country **name**, with a generic fallback.
 * @param {string} countryName
 * @param {string} nationalId
 * @returns {boolean}
 */
export function validateNationalID(countryName, nationalId) {
  const key = String(countryName).trim().toLowerCase();
  const pattern = namePatterns[key] || genericPattern;
  return pattern.test(String(nationalId).trim());
}
